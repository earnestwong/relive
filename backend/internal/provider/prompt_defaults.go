// PromptConfig 提示词配置（用于 provider）
type PromptConfig struct {
	AnalysisPrompt string `json:"analysis_prompt" yaml:"analysis_prompt,omitempty"`
	CaptionPrompt  string `json:"caption_prompt" yaml:"caption_prompt,omitempty"`
	BatchPrompt    string `json:"batch_prompt" yaml:"batch_prompt,omitempty"`
}

// GetAnalysisPrompt 获取分析提示词，如果未设置则返回默认值
func (pc *PromptConfig) GetAnalysisPrompt() string {
	if pc != nil && pc.AnalysisPrompt != "" {
		return pc.AnalysisPrompt
	}
	return DefaultAnalysisPrompt
}

// GetCaptionPrompt 获取文案生成提示词，如果未设置则返回默认值
func (pc *PromptConfig) GetCaptionPrompt() string {
	if pc != nil && pc.CaptionPrompt != "" {
		return pc.CaptionPrompt
	}
	return DefaultCaptionPrompt
}

// GetBatchPrompt 获取批量分析提示词，如果未设置则返回默认值
func (pc *PromptConfig) GetBatchPrompt() string {
	if pc != nil && pc.BatchPrompt != "" {
		return pc.BatchPrompt
	}
	return DefaultBatchPrompt
}
